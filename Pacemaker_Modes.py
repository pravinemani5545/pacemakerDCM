class AOO:

    def __init__(self):
        self.LRL = 0        # lower rate limit
        self.URL = 0        # upper rate limit
        self.AA = 0         # atrial amplitude
        self.APW = 0        # atrial pulse width

    def set_LRL(self, value):
        self.LRL = value

    def get_LRL(self):
        return self.LRL

    def set_URL(self, value):
        self.URL = value

    def get_URL(self):
        return self.URL

    def set_AA(self, value):
        self.AA = value

    def get_AA(self):
        return self.AA

    def set_APW(self, value):
        self.APW = value

    def get_APW(self):
        return self.APW

